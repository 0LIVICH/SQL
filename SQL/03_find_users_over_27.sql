-- Скрипт поиска пользователей старше 27 лет
-- Выбирает все поля и сортирует результаты по убыванию возраста

SELECT name, surname, age, phone_number, city_of_living
FROM PERSONS
WHERE age > 27
ORDER BY age DESC;

-- Комментарий: Запрос возвращает все поля для пользователей,
-- возраст которых больше 27 лет, отсортированные по убыванию возраста 